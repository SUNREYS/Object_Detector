"""
=============================================================================
Evaluation Script: Subgroup Analysis Using Preprocessing Metadata
=============================================================================

This script evaluates YOLOv8 predictions against ground truth on the
validation set, then merges results with the preprocessing metadata CSVs
to analyse detection performance across subgroups.

The goal is to determine whether certain subgroups are systematically
harder to detect, which helps assess the impact of the thermal-to-visible
translation model on different object categories.

Subgroups analysed:
    - Day vs Night (from KAIST set ID mapping)
    - Object size (small / medium / large)
    - Occlusion level (none / partial / heavy)
    - Crowd density (number of people per frame)
    - Truncation (truncated vs not)
    - Overlap (has overlapping neighbour vs not)

Output CSVs  (saved to OD/eval_results/):
    per_image_results_{modality}.csv   -- per-image TP/FP/FN + frame metadata
    per_object_results_{modality}.csv  -- per-GT-object detected/missed + metadata
    subgroup_summary_{modality}.csv    -- aggregated metrics by subgroup

Usage:
    python evaluate.py                                        # visible, best.pt
    python evaluate.py --modality thermal                     # thermal model
    python evaluate.py --weights runs/kaist_visible/weights/best.pt
    python evaluate.py --conf 0.3                             # custom confidence
=============================================================================
"""

